    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        LocalDateTime lichSuDung = dateTimePicker2.getDateTimeStrict();
        if (jLabel3.getText().equals("Số tiền:")){
            JOptionPane.showMessageDialog(this, "Hãy tiến hành kiểm tra phòng hỗ trợ dịch vụ trước khi đăng ký");
        }
        else if(lichSuDung == null){
            JOptionPane.showMessageDialog(this, "Cần chọn đầy đủ thông tin lịch sử dụng");
        }
        else if(jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Cần nhập đầy đủ thông tin thanh toán");
        }
        else{
            String labelText = jLabel3.getText();
            int index = labelText.indexOf(":");
            String numberText = labelText.substring(index + 1).trim();
            BigDecimal giaDichVu = new BigDecimal(numberText);
            BUS_TINHPHIDICHVUTAMTHOI busTinhPhiDichVuTamThoi = new BUS_TINHPHIDICHVUTAMTHOI();
            busTinhPhiDichVuTamThoi.TinhPhiDichVuTamThoi(jTextField3.getText(),jTextField4.getText(),giaDichVu);
            BUS_DANGKYDICHVU busDangKyDichVu = new BUS_DANGKYDICHVU();
            busDangKyDichVu.ThemThongTinDangKy(new BUS_DANGKYDICHVU((String) jComboBox1.getSelectedItem(), jTextField1.getText(), lichSuDung, giaDichVu));
            JOptionPane.showMessageDialog(this, "Đăng ký thành công");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

