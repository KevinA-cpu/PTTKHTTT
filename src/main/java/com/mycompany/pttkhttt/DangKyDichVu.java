    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id_kh = jTextField1.getText();
        String id_phong = jTextField2.getText();
        BUS_DANGKYDICHVU busDangKyDichVu = new BUS_DANGKYDICHVU();
        if(busDangKyDichVu.KiemTraIdKhachHangVaIdPhong(id_kh, id_phong)){
            BUS_DICHVU dv =  new BUS_DICHVU();
            String id_dv = (String) jComboBox1.getSelectedItem();
            String apdung = dv.LayApDungDichVu(id_dv);
            BigDecimal giaDichVu = dv.layGiaDichVu(id_dv);
            if(busDangKyDichVu.KiemTraIdPhongHoTroDichVu(id_phong, apdung)){
                JOptionPane.showMessageDialog(this, "Phòng hỗ trợ dịch vụ, không tính thêm phí dùng dịch vụ");
                jLabel3.setText("Số tiền: 20000");
            }
            else{
                JOptionPane.showMessageDialog(this, "Phòng không hỗ trợ dịch vụ, sẽ tính thêm phí dùng dịch vụ");
                giaDichVu = giaDichVu.add(new BigDecimal("20000"));
                jLabel3.setText("Số tiền: " + giaDichVu.toString());
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Không tồn tại thông tin đặt phòng");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

